/*
** Calculator bitwise extension -- shared library plugin.
**
** Exports a CalcBitwisePlugin struct via calc_bitwise_entry().  The host
** program loads this .so with dlopen, calls calc_bitwise_entry() to get
** the plugin descriptor, and uses the eval() function to parse
** expressions that include bitwise operators (&, |, ^, <<, >>).
**
** The ^ (CARET) token is interpreted as bitwise XOR here, which
** intentionally conflicts with the power extension where ^ means
** exponentiation.  This demonstrates grammar extension conflicts.
*/
#include "tokenize.h"
#include "calc_bitwise.h"

#include <stdlib.h>
#include <string.h>
#include <stdint.h>

/*
** Lime parser interface functions (generated by Lime).
** Prefixed with the %name directive value: CalcBitwise.
*/
void *CalcBitwiseAlloc(void *(*mallocProc)(size_t));
void  CalcBitwiseFree(void *p, void (*freeProc)(void *));
void  CalcBitwise(void *yyp, int yymajor, int yyminor, int *result);

/* ------------------------------------------------------------------ */
/*  Extension descriptor (GrammarModification pattern)                  */
/* ------------------------------------------------------------------ */

typedef enum {
    MOD_ADD_RULE           = 0,
    MOD_ADD_TOKEN          = 1,
    MOD_MODIFY_PRECEDENCE  = 2,
} CalcGrammarModType;

typedef struct {
    CalcGrammarModType type;
    const char *description;

    union {
        struct {
            const char *lhs;
            const char **rhs;
            int nrhs;
            const char *code;
            int precedence;
        } add_rule;

        struct {
            const char *name;
            const char *lexeme;
            int token_code;
        } add_token;

        struct {
            const char *symbol;
            int new_precedence;
            int new_assoc; /* 0=none, 1=left, 2=right, 3=nonassoc */
        } modify_prec;
    } u;
} CalcGrammarModification;

