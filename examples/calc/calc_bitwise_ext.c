#define BITWISE_MOD_COUNT (sizeof(bitwise_mods) / sizeof(bitwise_mods[0]))

/* ------------------------------------------------------------------ */
/*  eval() -- tokenize and parse with the CalcBitwise parser            */
/* ------------------------------------------------------------------ */

static int bitwise_eval(const char *expr, int *out) {
    void *parser = CalcBitwiseAlloc(malloc);
    if (!parser) return -1;

    int result = 0;
    int pos = 0;
    int len = (int)strlen(expr);

    while (pos < len) {
        int value = 0;
        int tok = calc_next_token(expr, &pos, len, &value);
        if (tok == 0) break;
        CalcBitwise(parser, tok, value, &result);
    }

    /* Signal end of input */
    CalcBitwise(parser, 0, 0, &result);
    CalcBitwiseFree(parser, free);

    *out = result;
    return 0;
}

/* ------------------------------------------------------------------ */
/*  Plugin entry point                                                  */
/* ------------------------------------------------------------------ */

typedef struct CalcBitwisePlugin {
    const char *name;
    const char *version;
    int (*eval)(const char *expr, int *result);
    const CalcGrammarModification *mods;
    uint32_t nmods;
} CalcBitwisePlugin;

static const CalcBitwisePlugin plugin = {
    .name    = "bitwise_operators",
    .version = "1.0",
    .eval    = bitwise_eval,
    .mods    = bitwise_mods,
    .nmods   = BITWISE_MOD_COUNT,
};

const CalcBitwisePlugin *calc_bitwise_entry(void) {
    return &plugin;
}
