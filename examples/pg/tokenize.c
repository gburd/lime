/* ======================================================================
** Skip whitespace and comments
**
** Returns true if any whitespace containing a newline was seen.
** This is needed for the string continuation check.
** ====================================================================== */

static bool skip_whitespace(PgScanner *s) {
    bool saw_newline = false;
    while (s->pos < s->length) {
        unsigned char c = (unsigned char)s->input[s->pos];

        if (is_space(c)) {
            if (is_newline(c)) saw_newline = true;
            s->pos++;
            continue;
        }

        /* SQL line comment: -- to end of line */
        if (c == '-' && s->pos + 1 < s->length && s->input[s->pos + 1] == '-') {
            s->pos += 2;
            while (s->pos < s->length && !is_newline((unsigned char)s->input[s->pos]))
                s->pos++;
            /* The newline itself is consumed on the next iteration */
            continue;
        }

        /* C-style block comment: / * ... * / with nesting */
        if (c == '/' && s->pos + 1 < s->length && s->input[s->pos + 1] == '*') {
            s->pos += 2;
            skip_block_comment(s);
            continue;
        }

        break;
    }
    return saw_newline;
}

static void skip_block_comment(PgScanner *s) {
    int depth = 1;
    while (s->pos < s->length && depth > 0) {
        unsigned char c = (unsigned char)s->input[s->pos];
        if (c == '/' && s->pos + 1 < s->length && s->input[s->pos + 1] == '*') {
            depth++;
            s->pos += 2;
        } else if (c == '*' && s->pos + 1 < s->length && s->input[s->pos + 1] == '/') {
            depth--;
            s->pos += 2;
        } else {
            s->pos++;
        }
    }
    if (depth > 0) {
        s->errmsg = "unterminated /* comment";
        s->errloc = (int)s->pos;
    }
}


/* ======================================================================
** Check for string continuation (SQL standard)
**
** After a closing quote, if whitespace with at least one newline
** is followed by another single quote, the string continues.
** ====================================================================== */

static bool check_string_continuation(PgScanner *s) {
    size_t saved = s->pos;
    bool saw_newline = false;

    /* Skip whitespace and comments, tracking newlines */
    while (s->pos < s->length) {
        unsigned char c = (unsigned char)s->input[s->pos];
        if (is_space(c)) {
            if (is_newline(c)) saw_newline = true;
            s->pos++;
            continue;
        }
        if (c == '-' && s->pos + 1 < s->length && s->input[s->pos + 1] == '-') {
            s->pos += 2;
            while (s->pos < s->length && !is_newline((unsigned char)s->input[s->pos]))
                s->pos++;
            continue;
        }
        if (c == '/' && s->pos + 1 < s->length && s->input[s->pos + 1] == '*') {
            s->pos += 2;
            skip_block_comment(s);
            continue;
        }
        break;
    }

    if (saw_newline && s->pos < s->length && s->input[s->pos] == '\'') {
        s->pos++;  /* consume the opening quote of continuation */
        return true;
    }

    /* No continuation -- restore position */
    s->pos = saved;
    return false;
}


/* ======================================================================
** String literal scanning
** ====================================================================== */

/*
** Scan a standard single-quoted string body (already past opening quote).
** Handles '' escaping and string continuation.
** Returns SCONST on success, 0 on error.
*/
static int scan_string_body(PgScanner *s, int start_loc) {
    (void)start_loc;
    litbuf_init(s);

    for (;;) {
        while (s->pos < s->length) {
            char c = s->input[s->pos];
            if (c == '\'') {
                s->pos++;
                /* Check for '' (embedded quote) */
                if (s->pos < s->length && s->input[s->pos] == '\'') {
                    litbuf_addchar(s, '\'');
                    s->pos++;
                    continue;
                }
                /* End of string body -- check for continuation */
                if (check_string_continuation(s)) {
                    /* Continue accumulating into the same literal */
                    continue;
                }
                return SCONST;
            }
            litbuf_addchar(s, c);
            s->pos++;
        }
        /* Reached EOF inside string */
        s->errmsg = "unterminated quoted string";
        s->errloc = start_loc;
        return 0;
    }
}

/*
** Scan an extended (E'...') string body with backslash escapes.
*/
static int scan_extended_string_body(PgScanner *s, int start_loc) {
    litbuf_init(s);

    for (;;) {
        while (s->pos < s->length) {
            unsigned char c = (unsigned char)s->input[s->pos];

            if (c == '\'') {
                s->pos++;
                /* Check for '' */
                if (s->pos < s->length && s->input[s->pos] == '\'') {
                    litbuf_addchar(s, '\'');
                    s->pos++;
                    continue;
                }
                /* End of string -- check continuation */
                if (check_string_continuation(s)) {
                    continue;
                }
                return SCONST;
            }

            if (c == '\\') {
                s->pos++;
                if (s->pos >= s->length) {
                    litbuf_addchar(s, '\\');
                    break;
                }
                unsigned char esc = (unsigned char)s->input[s->pos];
                switch (esc) {
                    case 'b':  litbuf_addchar(s, '\b'); s->pos++; break;
                    case 'f':  litbuf_addchar(s, '\f'); s->pos++; break;
                    case 'n':  litbuf_addchar(s, '\n'); s->pos++; break;
                    case 'r':  litbuf_addchar(s, '\r'); s->pos++; break;
                    case 't':  litbuf_addchar(s, '\t'); s->pos++; break;
                    case 'v':  litbuf_addchar(s, '\v'); s->pos++; break;
                    case '\\': litbuf_addchar(s, '\\'); s->pos++; break;
                    case '\'': litbuf_addchar(s, '\''); s->pos++; break;

                    /* Octal escape: \NNN */
                    case '0': case '1': case '2': case '3':
                    case '4': case '5': case '6': case '7': {
                        unsigned int val = esc - '0';
                        s->pos++;
                        if (s->pos < s->length && is_octdigit((unsigned char)s->input[s->pos])) {
                            val = val * 8 + ((unsigned char)s->input[s->pos] - '0');
                            s->pos++;
                            if (s->pos < s->length && is_octdigit((unsigned char)s->input[s->pos])) {
                                val = val * 8 + ((unsigned char)s->input[s->pos] - '0');
                                s->pos++;
                            }
                        }
                        litbuf_addchar(s, (char)(unsigned char)val);
                        break;
                    }

                    /* Hex escape: \xNN */
                    case 'x': {
                        s->pos++;
                        if (s->pos < s->length && is_hexdigit((unsigned char)s->input[s->pos])) {
                            unsigned int val = 0;
                            unsigned char h = (unsigned char)s->input[s->pos];
                            if (h >= '0' && h <= '9') val = h - '0';
                            else if (h >= 'a' && h <= 'f') val = h - 'a' + 10;
                            else val = h - 'A' + 10;
                            s->pos++;
                            if (s->pos < s->length && is_hexdigit((unsigned char)s->input[s->pos])) {
                                h = (unsigned char)s->input[s->pos];
                                val *= 16;
                                if (h >= '0' && h <= '9') val += h - '0';
                                else if (h >= 'a' && h <= 'f') val += h - 'a' + 10;
                                else val += h - 'A' + 10;
                                s->pos++;
                            }
                            litbuf_addchar(s, (char)(unsigned char)val);
                        } else {
                            litbuf_addchar(s, 'x');
                        }
                        break;
                    }

                    /* Unicode escapes: \uXXXX or \UXXXXXXXX */
                    case 'u': case 'U': {
                        int ndigits = (esc == 'u') ? 4 : 8;
                        s->pos++;
                        /* For this initial implementation, just pass the
                        ** Unicode escape through as-is.  Full conversion
                        ** to UTF-8 can be added later. */
                        litbuf_addchar(s, '\\');
                        litbuf_addchar(s, (char)esc);
                        for (int i = 0; i < ndigits && s->pos < s->length; i++) {
                            if (!is_hexdigit((unsigned char)s->input[s->pos])) break;
                            litbuf_addchar(s, s->input[s->pos]);
                            s->pos++;
                        }
                        break;
                    }

                    default:
                        litbuf_addchar(s, (char)esc);
                        s->pos++;
                        break;
                }
                continue;
            }

            litbuf_addchar(s, (char)c);
            s->pos++;
        }
        s->errmsg = "unterminated quoted string";
        s->errloc = start_loc;
        return 0;
    }
}

/* Dollar-quoted string scanning is handled inline in pg_scan() */

/*
** Scan bit/hex string body (B'...' or X'...').
** prefix is 'b' or 'x'.
*/
static int scan_bitstring_body(PgScanner *s, char prefix, int start_loc) {
    litbuf_init(s);
    litbuf_addchar(s, prefix);

    while (s->pos < s->length) {
        char c = s->input[s->pos];
        if (c == '\'') {
            s->pos++;
            /* Check for '' */
            if (s->pos < s->length && s->input[s->pos] == '\'') {
                litbuf_addchar(s, '\'');
                s->pos++;
                continue;
            }
            /* Check for continuation */
            if (check_string_continuation(s)) {
                continue;
            }
            return (prefix == 'b') ? BCONST : XCONST;
        }
        litbuf_addchar(s, c);
        s->pos++;
    }
    s->errmsg = (prefix == 'b')
        ? "unterminated bit string literal"
        : "unterminated hexadecimal string literal";
    s->errloc = start_loc;
    return 0;
}

/*
** Scan a double-quoted (delimited) identifier.
** Called after the opening " has been consumed.
*/
static int scan_delimited_ident(PgScanner *s, int start_loc) {
    litbuf_init(s);

    while (s->pos < s->length) {
        char c = s->input[s->pos];
        if (c == '"') {
            s->pos++;
            /* Check for "" */
            if (s->pos < s->length && s->input[s->pos] == '"') {
                litbuf_addchar(s, '"');
                s->pos++;
                continue;
            }
            if (s->litlen == 0) {
                s->errmsg = "zero-length delimited identifier";
                s->errloc = start_loc;
                return 0;
            }
            return IDENT;
        }
        litbuf_addchar(s, c);
        s->pos++;
    }
    s->errmsg = "unterminated quoted identifier";
    s->errloc = start_loc;
    return 0;
}

/*
** Scan a Unicode-escape identifier: U&"..."
** Called after 'U&"' has been consumed.
*/
static int scan_unicode_ident(PgScanner *s, int start_loc) {
    litbuf_init(s);

    while (s->pos < s->length) {
        char c = s->input[s->pos];
        if (c == '"') {
            s->pos++;
            if (s->pos < s->length && s->input[s->pos] == '"') {
                litbuf_addchar(s, '"');
                s->pos++;
                continue;
            }
            if (s->litlen == 0) {
                s->errmsg = "zero-length delimited identifier";
                s->errloc = start_loc;
                return 0;
            }
            return UIDENT;
        }
        litbuf_addchar(s, c);
        s->pos++;
    }
    s->errmsg = "unterminated quoted identifier";
    s->errloc = start_loc;
    return 0;
}

/*
** Scan a Unicode-escape string: U&'...'
** Called after U&' has been consumed.
*/
static int scan_unicode_string(PgScanner *s, int start_loc) {
    litbuf_init(s);

    while (s->pos < s->length) {
        char c = s->input[s->pos];
        if (c == '\'') {
            s->pos++;
            if (s->pos < s->length && s->input[s->pos] == '\'') {
                litbuf_addchar(s, '\'');
                s->pos++;
                continue;
            }
            if (check_string_continuation(s)) {
                continue;
            }
            return USCONST;
        }
        litbuf_addchar(s, c);
        s->pos++;
    }
    s->errmsg = "unterminated quoted string";
    s->errloc = start_loc;
    return 0;
}


/* ======================================================================
** Number scanning helpers
** ====================================================================== */

/*
** Scan decimal digits with optional underscore separators.
** Returns number of digits consumed (not counting underscores).
*/
static int scan_digits(PgScanner *s, bool (*is_valid)(unsigned char)) {
    int count = 0;
    while (s->pos < s->length) {
        unsigned char c = (unsigned char)s->input[s->pos];
        if (is_valid(c)) {
            count++;
            s->pos++;
        } else if (c == '_' && count > 0) {
            /* Underscore must be followed by a valid digit */
            if (s->pos + 1 < s->length && is_valid((unsigned char)s->input[s->pos + 1])) {
                s->pos++;  /* skip underscore */
            } else {
                break;
            }
        } else {
            break;
        }
    }
    return count;
}

/*
** Try to parse the accumulated number text as an int32.
** Returns true if it fits, false if overflow.
*/
static bool try_parse_int32(const char *text, size_t len, int base, int32_t *out) {
    /* Copy to NUL-terminated buffer, stripping underscores */
    char buf[128];
    size_t j = 0;
    size_t start = 0;

    /* Skip base prefix */
    if (base != 10 && len >= 2) {
        start = 2;  /* skip 0x, 0o, 0b */
    }

    for (size_t i = start; i < len && j < sizeof(buf) - 1; i++) {
        if (text[i] != '_')
            buf[j++] = text[i];
    }
    buf[j] = '\0';

    errno = 0;
    char *end;
    long val = strtol(buf, &end, base);
    if (errno == ERANGE || val < INT32_MIN || val > INT32_MAX)
        return false;
    if (*end != '\0')
        return false;
    *out = (int32_t)val;
    return true;
}


/* ======================================================================
** Main scanner function
**
** pg_scan() extracts the next token from the input.
**
** Returns:
**   > 0: Token code
**   0:   End of input
**   -1:  Error (check s->errmsg)
**
** On success, fills in *val with the semantic value and *loc with
** the byte offset of the token start.
** ====================================================================== */

int pg_scan(PgScanner *s, PgTokenValue *val, int *loc) {
    unsigned char c;

restart:
    skip_whitespace(s);

    if (s->pos >= s->length) {
        *loc = (int)s->pos;
        return 0;  /* EOF */
    }

    *loc = (int)s->pos;
    c = (unsigned char)s->input[s->pos];

    /* ---- Identifiers and keywords ---- */
    if (is_ident_start(c)) {
        size_t start = s->pos;
        s->pos++;
        while (s->pos < s->length && is_ident_cont((unsigned char)s->input[s->pos]))
            s->pos++;
        size_t len = s->pos - start;

        /* Check for string-type prefixes: B'', X'', E'', N'', U&'', U&"" */
        if (len == 1 && s->pos < s->length && s->input[s->pos] == '\'') {
            unsigned char first = to_lower((unsigned char)s->input[start]);
            if (first == 'b') {
                s->pos++;  /* consume quote */
                int tok = scan_bitstring_body(s, 'b', (int)start);
                if (tok == 0) return -1;
                val->str = litbuf_dup(s);
                return tok;
            }
            if (first == 'x') {
                s->pos++;
                int tok = scan_bitstring_body(s, 'x', (int)start);
                if (tok == 0) return -1;
                val->str = litbuf_dup(s);
                return tok;
            }
            if (first == 'e') {
                s->pos++;
                int tok = scan_extended_string_body(s, (int)start);
                if (tok == 0) return -1;
                val->str = litbuf_dup(s);
                return tok;
            }
            if (first == 'n') {
                /* National character: treat as keyword NCHAR + push back quote */
                s->pos = start + 1;  /* back to just after 'n' */
                int kwtoken = pg_keyword_lookup("nchar", 5);
                if (kwtoken > 0) {
                    val->keyword = pg_keyword_name(kwtoken);
                    return kwtoken;
                }
                /* NCHAR not a keyword -- return 'n' as identifier */
                val->str = strdup("n");
                return IDENT;
            }
        }

        /* Check for U& prefix (Unicode escapes) */
        if (len == 1 && to_lower((unsigned char)s->input[start]) == 'u' &&
            s->pos < s->length && s->input[s->pos] == '&') {
            if (s->pos + 1 < s->length) {
                char next = s->input[s->pos + 1];
                if (next == '\'') {
                    s->pos += 2;  /* skip &' */
                    int tok = scan_unicode_string(s, (int)start);
                    if (tok == 0) return -1;
                    val->str = litbuf_dup(s);
                    return tok;
                }
                if (next == '"') {
                    s->pos += 2;  /* skip &" */
                    int tok = scan_unicode_ident(s, (int)start);
                    if (tok == 0) return -1;
                    val->str = litbuf_dup(s);
                    return tok;
                }
            }
            /* Not U& followed by quote -- fall through to identifier */
        }

        /* Keyword lookup */
        int kwtoken = pg_keyword_lookup(s->input + start, len);
        if (kwtoken > 0) {
            val->keyword = pg_keyword_name(kwtoken);
            return kwtoken;
        }

        /* Regular identifier: lowercase and truncate */
        size_t ident_len = (len < PG_NAMEDATALEN - 1) ? len : PG_NAMEDATALEN - 1;
        char *ident = malloc(ident_len + 1);
        if (!ident) return -1;
        for (size_t i = 0; i < ident_len; i++)
            ident[i] = (char)to_lower((unsigned char)s->input[start + i]);
        ident[ident_len] = '\0';
        val->str = ident;
        return IDENT;
    }

    /* ---- Numeric literals ---- */
    if (is_digit(c) || (c == '.' && s->pos + 1 < s->length &&
                        is_digit((unsigned char)s->input[s->pos + 1]))) {
        size_t start = s->pos;
        bool is_float = false;
        int base = 10;

        if (c == '0' && s->pos + 1 < s->length) {
            unsigned char next = to_lower((unsigned char)s->input[s->pos + 1]);
            if (next == 'x') {
                base = 16; s->pos += 2;
                if (s->pos < s->length && s->input[s->pos] == '_') s->pos++;
                if (scan_digits(s, is_hexdigit) == 0) {
                    s->errmsg = "invalid hexadecimal integer";
                    s->errloc = (int)start;
                    return -1;
                }
                goto check_integer_junk;
            }
            if (next == 'o') {
                base = 8; s->pos += 2;
                if (s->pos < s->length && s->input[s->pos] == '_') s->pos++;
                if (scan_digits(s, is_octdigit) == 0) {
                    s->errmsg = "invalid octal integer";
                    s->errloc = (int)start;
                    return -1;
                }
                goto check_integer_junk;
            }
            if (next == 'b' && s->pos + 2 < s->length &&
                is_bindigit((unsigned char)s->input[s->pos + 2])) {
                /* Only treat as binary if followed by binary digit
                ** (to distinguish from 0b being start of identifier) */
                base = 2; s->pos += 2;
                if (s->pos < s->length && s->input[s->pos] == '_') s->pos++;
                if (scan_digits(s, is_bindigit) == 0) {
                    s->errmsg = "invalid binary integer";
                    s->errloc = (int)start;
                    return -1;
                }
                goto check_integer_junk;
            }
        }

        /* Decimal integer or start of numeric/real */
        if (c != '.') {
            scan_digits(s, is_digit);
        }

        /* Check for decimal point */
        if (s->pos < s->length && s->input[s->pos] == '.') {
            /* Check for '..' (DOT_DOT) - don't consume the dots */
            if (s->pos + 1 < s->length && s->input[s->pos + 1] == '.') {
                /* This is numericfail: return the integer part, leave '..' */
                goto finish_integer;
            }
            is_float = true;
            s->pos++;  /* consume '.' */
            scan_digits(s, is_digit);
        } else if (c == '.') {
            is_float = true;
            s->pos++;  /* consume leading '.' */
            scan_digits(s, is_digit);
        }

        /* Check for exponent */
        if (s->pos < s->length &&
            (s->input[s->pos] == 'e' || s->input[s->pos] == 'E')) {
            size_t exp_start = s->pos;
            s->pos++;
            if (s->pos < s->length &&
                (s->input[s->pos] == '+' || s->input[s->pos] == '-')) {
                s->pos++;
            }
            if (s->pos >= s->length || !is_digit((unsigned char)s->input[s->pos])) {
                /* realfail: no digits after exponent */
                if (is_float) {
                    s->errmsg = "trailing junk after numeric literal";
                    s->errloc = (int)exp_start;
                    return -1;
                }
                /* Revert: not an exponent */
                s->pos = exp_start;
                goto finish_integer;
            }
            is_float = true;
            scan_digits(s, is_digit);
        }

        if (is_float) {
            /* Check for trailing identifier chars (junk) */
            if (s->pos < s->length && is_ident_start((unsigned char)s->input[s->pos])) {
                s->errmsg = "trailing junk after numeric literal";
                s->errloc = (int)s->pos;
                return -1;
            }
            size_t len = s->pos - start;
            val->str = strndup(s->input + start, len);
            return FCONST;
        }

    finish_integer:
        ;  /* empty statement after label */

    check_integer_junk:
        /* Check for trailing identifier chars */
        if (s->pos < s->length && is_ident_start((unsigned char)s->input[s->pos])) {
            s->errmsg = "trailing junk after numeric literal";
            s->errloc = (int)s->pos;
            return -1;
        }

        size_t numlen = s->pos - start;
        int32_t ival;
        if (try_parse_int32(s->input + start, numlen, base, &ival)) {
            val->ival = ival;
            return ICONST;
        }
        /* Overflow: treat as float */
        val->str = strndup(s->input + start, numlen);
        return FCONST;
    }

    /* ---- String literals ---- */
    if (c == '\'') {
        s->pos++;
        int tok = scan_string_body(s, (int)(s->pos - 1));
        if (tok == 0) return -1;
        val->str = litbuf_dup(s);
        return tok;
    }

    /* ---- Dollar-quoted strings ---- */
    if (c == '$') {
        /* Check for $tag$ pattern */
        size_t dstart = s->pos;
        s->pos++;  /* past initial $ */

        /* Read optional tag */
        size_t tag_start = s->pos;
        if (s->pos < s->length && is_ident_start((unsigned char)s->input[s->pos])) {
            s->pos++;
            while (s->pos < s->length && is_ident_cont((unsigned char)s->input[s->pos]) &&
                   s->input[s->pos] != '$')
                s->pos++;
        }

        if (s->pos < s->length && s->input[s->pos] == '$') {
            /* We have a valid dollar-quote delimiter */
            size_t delim_len = s->pos - dstart + 1;
            s->pos++;  /* past closing $ of delimiter */

            /* Save the delimiter for matching */
            char *delim = strndup(s->input + dstart, delim_len);
            if (!delim) return -1;

            /* Scan for matching delimiter */
            litbuf_init(s);
            while (s->pos < s->length) {
                if (s->input[s->pos] == '$') {
                    /* Check if this starts the closing delimiter */
                    if (s->pos + delim_len <= s->length &&
                        memcmp(s->input + s->pos, delim, delim_len) == 0) {
                        s->pos += delim_len;
                        free(delim);
                        val->str = litbuf_dup(s);
                        return SCONST;
                    }
                }
                litbuf_addchar(s, s->input[s->pos]);
                s->pos++;
            }
            free(delim);
            s->errmsg = "unterminated dollar-quoted string";
            s->errloc = (int)dstart;
            return -1;
        }

        /* Not a dollar-quote delimiter.  Check for $N parameter. */
        s->pos = dstart + 1;  /* back to after the $ */
        if (s->pos < s->length && is_digit((unsigned char)s->input[s->pos])) {
            size_t pstart = s->pos;
            while (s->pos < s->length && is_digit((unsigned char)s->input[s->pos]))
                s->pos++;
            /* Check for junk */
            if (s->pos < s->length && is_ident_start((unsigned char)s->input[s->pos])) {
                s->errmsg = "trailing junk after parameter";
                s->errloc = (int)dstart;
                return -1;
            }
            char *pstr = strndup(s->input + pstart, s->pos - pstart);
            if (!pstr) return -1;
            errno = 0;
            long pnum = strtol(pstr, NULL, 10);
            free(pstr);
            if (errno == ERANGE || pnum > INT32_MAX || pnum < 0) {
                s->errmsg = "parameter number too large";
                s->errloc = (int)dstart;
                return -1;
            }
            val->ival = (int32_t)pnum;
            return PARAM;
        }

        /* Plain $ -- return as single character token.
        ** The grammar does not use bare $, so this will be a syntax error. */
        s->pos = dstart + 1;
        (void)tag_start;
        return self_to_token('$');
    }

    /* ---- Double-quoted identifier ---- */
    if (c == '"') {
        s->pos++;
        int tok = scan_delimited_ident(s, (int)(s->pos - 1));
        if (tok == 0) return -1;
        val->str = litbuf_dup(s);
        return tok;
    }

    /* ---- Multi-character operators (check before single-char) ---- */

    /* :: (TYPECAST) */
    if (c == ':' && s->pos + 1 < s->length && s->input[s->pos + 1] == ':') {
        s->pos += 2;
        return TYPECAST;
    }

    /* := (COLON_EQUALS) */
    if (c == ':' && s->pos + 1 < s->length && s->input[s->pos + 1] == '=') {
        s->pos += 2;
        return COLON_EQUALS;
    }

    /* .. (DOT_DOT) */
    if (c == '.' && s->pos + 1 < s->length && s->input[s->pos + 1] == '.') {
        s->pos += 2;
        return DOT_DOT;
    }

    /* => (EQUALS_GREATER) */
    if (c == '=' && s->pos + 1 < s->length && s->input[s->pos + 1] == '>') {
        s->pos += 2;
        return EQUALS_GREATER;
    }

    /* >= (GREATER_EQUALS) */
    if (c == '>' && s->pos + 1 < s->length && s->input[s->pos + 1] == '=') {
        s->pos += 2;
        return GREATER_EQUALS;
    }

    /* <= (LESS_EQUALS) */
    if (c == '<' && s->pos + 1 < s->length && s->input[s->pos + 1] == '=') {
        s->pos += 2;
        return LESS_EQUALS;
    }

    /* <> (NOT_EQUALS) */
    if (c == '<' && s->pos + 1 < s->length && s->input[s->pos + 1] == '>') {
        s->pos += 2;
        return NOT_EQUALS;
    }

    /* != (NOT_EQUALS) */
    if (c == '!' && s->pos + 1 < s->length && s->input[s->pos + 1] == '=') {
        s->pos += 2;
        return NOT_EQUALS;
    }

    /* -> (RIGHT_ARROW) */
    if (c == '-' && s->pos + 1 < s->length && s->input[s->pos + 1] == '>') {
        s->pos += 2;
        return RIGHT_ARROW;
    }

    /* ---- Generic operator ---- */
    if (is_op_char(c) && !is_self(c)) {
        /* This character is an op_char but not a self char, so it must
        ** start an operator (e.g. ~, !, @, #, &, `, ?) */
        goto scan_operator;
    }

    /* For characters that are in BOTH op_chars and self, handle them
    ** as operators only if followed by another op_char. */
    if (is_op_char(c) && is_self(c)) {
        if (s->pos + 1 < s->length && is_op_char((unsigned char)s->input[s->pos + 1])) {
            /* But first check for comment starts */
            if (c == '-' && s->input[s->pos + 1] == '-') {
                /* Line comment -- should have been caught by skip_whitespace.
                ** This shouldn't happen, but handle gracefully. */
                goto restart;
            }
            if (c == '/' && s->input[s->pos + 1] == '*') {
                goto restart;
            }
            goto scan_operator;
        }
        /* Single self character */
        s->pos++;
        return self_to_token(c);
    }

    if (is_self(c)) {
        s->pos++;
        return self_to_token(c);
    }

    /* ---- Scan a multi-character operator ---- */
    if (false) {
    scan_operator:
        ;
        size_t op_start = s->pos;
        s->pos++;
        while (s->pos < s->length && is_op_char((unsigned char)s->input[s->pos])) {
            /* Check for embedded comment starts */
            if (s->input[s->pos] == '/' && s->pos + 1 < s->length &&
                s->input[s->pos + 1] == '*') break;
            if (s->input[s->pos] == '-' && s->pos + 1 < s->length &&
                s->input[s->pos + 1] == '-') break;
            s->pos++;
        }

        size_t nchars = s->pos - op_start;

        /* SQL compatibility: + and - cannot end a multi-char operator
        ** unless it contains a "special" op char */
        if (nchars > 1) {
            char last = s->input[s->pos - 1];
            if (last == '+' || last == '-') {
                bool has_special = false;
                for (size_t i = op_start; i < s->pos - 1; i++) {
                    if (is_special_op_char((unsigned char)s->input[i])) {
                        has_special = true;
                        break;
                    }
                }
                if (!has_special) {
                    /* Strip trailing + and - */
                    while (nchars > 1) {
                        char ch = s->input[op_start + nchars - 1];
                        if (ch != '+' && ch != '-') break;
                        nchars--;
                    }
                    s->pos = op_start + nchars;
                }
            }
        }

        /* If reduced to single self char, return it as character token */
        if (nchars == 1 && is_self((unsigned char)s->input[op_start])) {
            return self_to_token((unsigned char)s->input[op_start]);
        }

        /* If reduced to a known 2-char token, return that */
        if (nchars == 2) {
            char c0 = s->input[op_start], c1 = s->input[op_start + 1];
            if (c0 == '=' && c1 == '>') return EQUALS_GREATER;
            if (c0 == '>' && c1 == '=') return GREATER_EQUALS;
            if (c0 == '<' && c1 == '=') return LESS_EQUALS;
            if (c0 == '<' && c1 == '>') return NOT_EQUALS;
            if (c0 == '!' && c1 == '=') return NOT_EQUALS;
            if (c0 == '-' && c1 == '>') return RIGHT_ARROW;
        }

        if (nchars >= PG_NAMEDATALEN) {
            s->errmsg = "operator too long";
            s->errloc = (int)op_start;
            return -1;
        }

        val->str = strndup(s->input + op_start, nchars);
        return OP;
    }

    /* ---- Unknown character ---- */
    s->pos++;
    return self_to_token(c);
}


