/*
** PostgreSQL SQL Tokenizer for Lime Parser
**
** Hand-written tokenizer that implements the lexical analysis described in
** PostgreSQL's scan.l, adapted for use with a Lime-generated parser.
**
** This tokenizer handles:
**   - SQL keywords (case-insensitive lookup via binary search)
**   - Identifiers (lowercased, truncated at PG_NAMEDATALEN)
**   - Integer, float, and numeric literals (decimal, hex, octal, binary)
**   - String literals (standard, extended E'', dollar-quoted, bit, hex, Unicode)
**   - C-style nested block comments and SQL line comments
**   - All PostgreSQL operator and punctuation tokens
**   - String continuation across newlines (SQL standard)
**   - Location tracking (byte offset)
**   - One-token lookahead for LALR(1) fixups (FORMAT_LA, NOT_LA, etc.)
**
** See LEXER_NOTES.md for the detailed analysis this is based on.
*/

#define _POSIX_C_SOURCE 200809L

#include "tokenize.h"
#include "pg_kwlist.h"         /* Auto-generated keyword table (uses pg_grammar.h) */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <errno.h>
#include <limits.h>

/* Maximum identifier length (same as PostgreSQL NAMEDATALEN) */
#define PG_NAMEDATALEN 64

/* Initial and growth factor for the literal buffer */
#define LITBUF_INIT 256
#define LITBUF_GROW 2


/* ======================================================================
** Keyword table
**
** The keyword table is provided by pg_kwlist.h (auto-generated by
** gen_kwlist.py from pg_grammar.h + gram.lime).  It contains 510
** entries sorted alphabetically, each with a Lime token ID, reservation
** category, and bare-label flag.
**
** The pg_kw_lookup() inline function from pg_kwlist.h performs binary
** search.  We wrap it below to match the public API declared in
** tokenize.h.
** ======================================================================
*/

/* ======================================================================
** Single-character token mapping
**
** Lime uses named tokens for all single characters that PostgreSQL's
** lexer returns as their ASCII value.  This function maps the character
** to the corresponding Lime token ID from pg_grammar.h.
** ====================================================================== */

static int self_to_token(unsigned char c) {
    switch (c) {
        case '(': return LPAREN;
        case ')': return RPAREN;
        case '[': return LBRACKET;
        case ']': return RBRACKET;
        case ',': return COMMA;
        case ';': return SEMICOLON;
        case ':': return COLON;
        case '.': return DOT;
        case '+': return PLUS;
        case '-': return MINUS;
        case '*': return STAR;
        case '/': return SLASH;
        case '%': return PERCENT;
        case '^': return CARET;
        case '<': return LT;
        case '>': return GT;
        case '=': return EQ;
        case '|': return PIPE;
        case '#': return HASH;
        default:  return self_to_token(c);  /* fallback for unexpected chars */
    }
}


/* Keyword lookup / name functions using pg_kwlist.h */

/* The keyword table is now in pg_kwlist.h (auto-generated). */

/*
** Case-insensitive keyword comparison for binary search.
** The pg_kwlist.h table is already lowercase; we lowercase input on the fly.
*/
static int kw_cmp_ci(const char *input, size_t len, const char *keyword) {
    for (size_t i = 0; i < len; i++) {
        unsigned char a = (unsigned char)input[i];
        unsigned char b = (unsigned char)keyword[i];
        if (b == '\0') return 1;   /* input longer than keyword */
        if (a >= 'A' && a <= 'Z') a += 32;
        if (a != b) return (int)a - (int)b;
    }
    if (keyword[len] != '\0') return -1;
    return 0;
}

/*
** Look up a keyword by name (case-insensitive).
** Returns the Lime token code, or 0 if not a keyword.
*/
int pg_keyword_lookup(const char *text, size_t len) {
    int lo = 0;
    int hi = PG_NUM_KEYWORDS - 1;
    while (lo <= hi) {
        int mid = lo + (hi - lo) / 2;
        int cmp = kw_cmp_ci(text, len, pg_keywords[mid].keyword);
        if (cmp < 0)
            hi = mid - 1;
        else if (cmp > 0)
            lo = mid + 1;
        else
            return pg_keywords[mid].token_id;
    }
    return 0;  /* not a keyword */
}

/*
** Get the canonical (lowercase) keyword name for a token code.
** Linear scan since token codes are not necessarily sequential.
*/
const char *pg_keyword_name(int token) {
    for (int i = 0; i < PG_NUM_KEYWORDS; i++) {
        if (pg_keywords[i].token_id == token)
            return pg_keywords[i].keyword;
    }
    return NULL;
}

/*
** Return the number of keywords in the table.
*/
int pg_keyword_count(void) {
    return PG_NUM_KEYWORDS;
}


/* ======================================================================
** Scanner state
** ====================================================================== */

struct PgScanner {
    const char *input;      /* Input SQL string */
    size_t      length;     /* Length of input */
    size_t      pos;        /* Current byte offset */

    /* Literal buffer for string accumulation */
    char       *litbuf;
    size_t      litlen;
    size_t      litalloc;

    /* Lookahead buffer for pg_scan_with_lookahead() */
    bool        have_lookahead;     /* True if lookahead token is buffered */
    int         la_token;           /* Buffered lookahead token code */
    PgTokenValue la_val;            /* Buffered lookahead semantic value */
    int         la_loc;             /* Buffered lookahead location */

    /* Error state */
    const char *errmsg;
    int         errloc;     /* Byte offset of error, or -1 */
};


/* ======================================================================
** Literal buffer helpers
** ====================================================================== */

static void litbuf_init(PgScanner *s) {
    s->litlen = 0;
}

static bool litbuf_ensure(PgScanner *s, size_t need) {
    size_t required = s->litlen + need;
    if (required < s->litalloc) return true;
    size_t newsize = s->litalloc;
    while (newsize <= required)
        newsize *= LITBUF_GROW;
    char *newbuf = realloc(s->litbuf, newsize);
    if (!newbuf) return false;
    s->litbuf = newbuf;
    s->litalloc = newsize;
    return true;
}

static void litbuf_add(PgScanner *s, const char *text, size_t len)
    __attribute__((unused));
static void litbuf_add(PgScanner *s, const char *text, size_t len) {
    if (!litbuf_ensure(s, len)) return;
    memcpy(s->litbuf + s->litlen, text, len);
    s->litlen += len;
}

static void litbuf_addchar(PgScanner *s, char c) {
    if (!litbuf_ensure(s, 1)) return;
    s->litbuf[s->litlen++] = c;
}

static char *litbuf_dup(PgScanner *s) {
    char *result = malloc(s->litlen + 1);
    if (!result) return NULL;
    memcpy(result, s->litbuf, s->litlen);
    result[s->litlen] = '\0';
    return result;
}


/* ======================================================================
** Character classification helpers
** ====================================================================== */

static inline bool is_space(unsigned char c) {
    return c == ' ' || c == '\t' || c == '\n' || c == '\r' ||
           c == '\f' || c == '\v';
}

static inline bool is_newline(unsigned char c) {
    return c == '\n' || c == '\r';
}

static inline bool is_ident_start(unsigned char c) {
    return (c >= 'A' && c <= 'Z') ||
           (c >= 'a' && c <= 'z') ||
           c == '_' || c >= 0x80;
}

static inline bool is_ident_cont(unsigned char c) {
    return (c >= 'A' && c <= 'Z') ||
           (c >= 'a' && c <= 'z') ||
           (c >= '0' && c <= '9') ||
           c == '_' || c == '$' || c >= 0x80;
}

static inline bool is_digit(unsigned char c) {
    return c >= '0' && c <= '9';
}

static inline bool is_hexdigit(unsigned char c) {
    return (c >= '0' && c <= '9') ||
           (c >= 'a' && c <= 'f') ||
           (c >= 'A' && c <= 'F');
}

static inline bool is_octdigit(unsigned char c) {
    return c >= '0' && c <= '7';
}

static inline bool is_bindigit(unsigned char c) {
    return c == '0' || c == '1';
}

/* Characters that are returned as single-character tokens */
static inline bool is_self(unsigned char c) {
    return c == ',' || c == '(' || c == ')' || c == '[' || c == ']' ||
           c == '.' || c == ';' || c == ':' || c == '|' || c == '+' ||
           c == '-' || c == '*' || c == '/' || c == '%' || c == '^' ||
           c == '<' || c == '>' || c == '=';
}

/* Characters that can appear in operators */
static inline bool is_op_char(unsigned char c) {
    return c == '~' || c == '!' || c == '@' || c == '#' || c == '^' ||
           c == '&' || c == '|' || c == '`' || c == '?' || c == '+' ||
           c == '-' || c == '*' || c == '/' || c == '%' || c == '<' ||
           c == '>' || c == '=';
}

/* Characters that make an operator "non-SQL-standard" (allowing trailing +/-) */
static inline bool is_special_op_char(unsigned char c) {
    return c == '~' || c == '!' || c == '@' || c == '#' || c == '^' ||
           c == '&' || c == '|' || c == '`' || c == '?' || c == '%';
}

static inline unsigned char to_lower(unsigned char c) {
    if (c >= 'A' && c <= 'Z') return c + 32;
    return c;
}


