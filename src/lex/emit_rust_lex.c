/*
** src/lex/emit_rust_lex.c -- Rust output for the .lex compiler.
**
** Mirror of lex_emit.c's C output but emitting Rust.  The DFA
** tables and per-state accept arrays are emitted as `pub static`
** slices; the runtime is a simple batch tokenizer
** `Lexer::tokenize(input: &str) -> Vec<Token>` rather than the
** streaming push API the C side ships.  Most parser consumers
** want a flat token stream and feed it into the LALR parser
** anyway, so the streaming complexity isn't on the critical
** path for v0.8.x.
**
** What we emit:
**
**   - per-state DFA tables:
**       pub static <STATE>_TRANS:  &[[i16; 256]] = &[...];
**       pub static <STATE>_ACCEPT: &[i16] = &[...];   (rule id or -1)
**   - rule-id constants:
**       pub const RULE_<NAME>: u16 = N;
**   - A `Token` struct: { rule_id, text_start, text_len, line, column }
**   - A `Lexer` struct with tokenize(&self, input: &str) -> Vec<Token>
**     plus per-state state-driven match.
**
** Action body translation: per-rule actions are emitted as
** Rust closures in a dispatch table.  Default action emits the
** token verbatim with the matched text.  Users override per-rule
** via `%rust_lex_action` (sibling to `%rust_action` on the parser
** side; the directive isn't part of v0.8.x and the action body
** gets a default `Token { rule_id, text, ... }` emit when absent).
*/

